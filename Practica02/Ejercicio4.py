num1 = int(input("Ingrese el primer numero "))
num2 = int(input("Ingrese el segundo numero "))

if num1 > num2:
    suma = num1 + num2
    print("La suma es: ", suma)
    resta = num1 - num2
    print("La resta es: ", resta)

else:
    x = num1 * num2
    print("El producto es: ",x)
    div = num1 / num2
    print("La division es: ",div)
