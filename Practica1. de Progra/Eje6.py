# AGREGAR, MODIFICAR Y ELIMINAR
# CREATE, READE, UDPATE, DELETE (CRUD)

vocales = ["a"]
print(vocales)

# AGREGAR
# APPEND()
vocales.append("e")
print(vocales)

# EXTEND()
vocales.extend(["o","u"])
print(vocales)

# INSERT()
vocales.insert(2, "i")
print(vocales)

# MODIFICAR
letras = ["b","b","c","d","e","ff"]
print(letras)
letras[0] = "a"
